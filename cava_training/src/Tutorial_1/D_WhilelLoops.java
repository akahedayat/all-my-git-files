package Tutorial_1;

public class D_WhilelLoops {
	/*
	 * what is while loops? in most computer programming languages, a while loop is
	 * a control flow statement that allows code to be executed repeatedly based on
	 * a given Boolean condition. The while loop can be thought of as a repeating if
	 * statement.
	 */

	public static void main(String[] args) {
		
		int value=0;
		while(value <10) {
			System.out.println(value);
			value=value +1;
			
		}
		
		boolean number1 = 9<11;
		System.out.println(number1);

		boolean number2 = 7<5;
		System.out.println(number2);
		
		int size =15;
		boolean loops=size>13;
		System.out.println(loops);
		
 
	}

}
