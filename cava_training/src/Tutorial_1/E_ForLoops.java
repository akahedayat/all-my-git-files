package Tutorial_1;

public class E_ForLoops {
	/*
	 * The Java for loop is a control flow statement that iterates a part of the
	 * programs multiple times. The Java while loop is a control flow statement that
	 * executes a part of the programs repeatedly on the basis of given boolean
	 * condition. ... If the number of iteration is not fixed, it is recommended to
	 * use while loop.
	 */

	public static void main(String[] args) {
		for (int i = 0; i < 5; i++) {
			System.out.println("The value of i is: " + i);

		}
		System.out.println("-------------------------1");
		for (int h = 3; h < 8; h++) {
			System.out.printf("The value of h is : %d\n ", h);
			 
			

		}

	}

}
