package OPSsConsept;

public class EncapsulationDemo {
	private int empSsn;
	private  String empName;
	private int empAge;
	public int getSsn() {
		return empSsn;
	}
	public void setSsn(int ssn) {
		this.empSsn = ssn;
	}
	public String getName() {
		return empName;
	}
	public void setName(String name) {
		this.empName = name;
	}
	public int getAge() {
		return empAge;
	}
	public void setAge(int age) {
		this.empAge = empAge;
	}
	
	
	

}
